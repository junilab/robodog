//% color="#AA278D" weight=100 icon="\uf0c3"
namespace main {
    /**
     * Adds two numbers
     */
    //% block
    export function addNumbers(a: number, b: number): number {
        return a + b;
    }
     /**
     * minus two numbers
     */
    //% block
    export function minusNumbers(a: number, b: number): number {
        return a - b;
    }
}
